/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	SCB_DEMCR |= CoreDebug_DEMCR_TRCENA_Msk; // разрешение счётчика
	DWT_CONTROL |= DWT_CTRL_CYCCNTENA_Msk;   // запуск счётчика
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_RTC_Init();
  //MX_USART3_UART_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  //_____поиск последовательности
  while (!state) {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  		while (!(GPIOB->IDR & GPIO_PIN_8)) { }//ждем фронт АМ

  		while (GPIOB->IDR & GPIO_PIN_8) { //всю длительность АМ пишем uint32 слово ФМ

  			DWT->CYCCNT = 0; // сброс счётчика тактов
  			word |= ((GPIOB->IDR & GPIO_PIN_9) >> 9)<< counter++; //пишем ФМ
  			while (DWT->CYCCNT < SYSTEM_CLOCK*2) {} //делаем 500KHz на сбор задавая делитель для системного клока
  		}

  		if((((word<<16)>>16)^searchWord) == 0){ //сверяем полученное слово с искомым
  			state=1;
  			flagGetDelay=1;
  			AMlength=(counter+1)*2;//устанавливаем длину выходного AM (+1 потому что теряется один такт), (*2 потому что частота дискретизации)
  			counter=0;
  		}
  		else{
  			counter=0;
  			word=0;
  		}

  	}


  	//в цикле выдаем тригер с АМ
  	while(m_delay[1]^WAITING_STATE){ //если в старшем бите принятого двухбайтного слова единица -> переводим в редим ожидания

  		//_____выдача АМ один в один на GPIO_PIN_5
  		   //GPIOA->ODR=((GPIOB->IDR & GPIO_PIN_8)>>8)<<5;

  		//_____совместить выдачу АМ с возможностью регулировать delay
  			if(GPIOB->IDR & GPIO_PIN_8){
  				DWT->CYCCNT = 0;
  				while (DWT->CYCCNT < (SYSTEM_CLOCK*1)) {} //ждем 1 микросекунды для отсечения коротких импульсов

  				if(GPIOB->IDR & GPIO_PIN_8){

  					//_____задержка выдачи тригера АМ
  					DWT->CYCCNT = 0;
  					while (DWT->CYCCNT < (SYSTEM_CLOCK* ( (uint16_t)m_delay[0] | ((uint16_t)(m_delay[1]<<8) ) ) ) ) {} //АМ выдается с заданной задержкой, описанной двумя
  					  																										  //байтами, принятыми из VCP

  					GPIOA->ODR=1<<5;
  					DWT->CYCCNT = 0;
  					while (DWT->CYCCNT < (SYSTEM_CLOCK*AMlength)) {}
  					GPIOA->ODR=0;
  				}
  			}
  	}

  	while(m_delay[1]^RESTART_STATE){} //ожидаем бит перезагрузки
  	//HAL_Delay(2000);//пока что через HAL
  	NVIC_SystemReset(); //reset микроконтроллера

  /* USER CODE END 3 */
}

